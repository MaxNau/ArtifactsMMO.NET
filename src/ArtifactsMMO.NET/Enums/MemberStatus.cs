namespace ArtifactsMMO.NET.Enums
{
    /// <summary>
    /// Represents the membership status of a user.
    /// </summary>
    public enum MemberStatus
    {
        /// <summary>
        /// Indicates that the member has a standard membership.
        /// </summary>
        Standard,

        /// <summary>
        /// Indicates that the member is a founder with gold status.
        /// </summary>
        GoldFounder,

        /// <summary>
        /// Indicates that the member is a founder with VIP status.
        /// </summary>
        VIPFounder,

        /// <summary>
        /// Indicates that the member is a founder.
        /// </summary>
        Founder
    }
}
