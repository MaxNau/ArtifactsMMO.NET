namespace ArtifactsMMO.NET.Enums
{
    /// <summary>
    /// Represents the types of tasks available in the game.
    /// </summary>
    /// <remarks>
    /// This enumeration categorizes tasks based on their nature, allowing players to differentiate between them.
    /// </remarks>
    public enum TaskType
    {
        /// <summary>
        /// A task that involves gathering or using items.
        /// </summary>
        Items,

        /// <summary>
        /// A task that involves engaging with monsters, either by defeating them or completing related objectives.
        /// </summary>
        Monsters
    }

}
